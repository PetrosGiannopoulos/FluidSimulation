	Vector3 VertexInterp1(float isoLevel, Vector3 p1, Vector3 p2, float v1, float v2) {
		
		float mu;
		Vector3 p;

		if (abs(isoLevel - v1) < 0.00001f)
			return(p1);
		if (abs(isoLevel - v2) < 0.00001f)
			return(p2);
		if (abs(v1 - v2) < 0.00001f)
			return(p1);
		mu = (isoLevel - v1) / (v2 - v1);
		p.x = p1.x + mu * (p2.x - p1.x);
		p.y = p1.y + mu * (p2.y - p1.y);
		p.z = p1.z + mu * (p2.z - p1.z);

		return(p);
	}

	Vector3 LinearInterp(Vector3 p1, Vector3 p2, float value)
	{
		return value*p1 + (1 - value)*p2;
	}

	bool lowerVector(Vector3 v1, Vector3 v2) {
		if (v1.x < v2.x)
			return true;
		else if (v1.x > v2.x)
			return false;

		if (v1.y < v2.y)
			return true;
		else if (v1.y > v2.y)
			return false;

		if (v1.z < v2.z)
			return true;
		else if (v1.z > v2.z)
			return false;

		return false;
	}

	Vector3 grad(Vector3 const& p) {
		auto const Eps = 1e-6f;
		Vector3 epsX{ Eps, 0, 0 }, epsY{ 0, Eps, 0 }, epsZ{ 0, 0, Eps };
		auto gx = (f(p + epsX) - f(p - epsX)) / 2;
		auto gy = (f(p + epsY) - f(p - epsY)) / 2;
		auto gz = (f(p + epsZ) - f(p - epsZ)) / 2;
		return Vector3(gx, gy, gz);
	}


