	Vector3 interpolate(const Vector3 &p1, const Vector3 &p2, float val1, float val2) {

		if (std::abs(val1) < 0.000001) return p1;
		if (std::abs(val2) < 0.000001) return p2;
		if (std::abs(val1 - val2) < 0.000001) return p1;

		float t = (isoLevel - val1) / (val2 - val1);

		return p1 + t * (p2 - p1);

	}

	Vector3 VertexInterp(float isoLevel, Vector3 p1, Vector3 p2, float v1, float v2)
	{
		if (lowerVector(p2,p1))
		{
			Vector3 temp;
			temp = p1;
			p1 = p2;
			p2 = temp;
		}

		Vector3 p;
		if (abs(v1 - v2) > 0.00001f)
			p = p1 + (p2 - p1) / (v2 - v1)*(isoLevel - v1);
		else
			p = p1;
		return p;
	}

